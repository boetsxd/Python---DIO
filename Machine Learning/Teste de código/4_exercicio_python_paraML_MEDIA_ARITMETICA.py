# Faça um algoritmo em linguagem Python que receba duas notas e calcule a média aritmética e mostre o resultado.

print('\t ----Calculo da media aritimetica----')

nota1 = int(input('Informe a primeira nota: '))
nota2 = int(input('Informe a segunda nora: '))

media = (nota1 +nota2)/2

print('\n  A média é:', media)